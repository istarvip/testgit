package com.walnutin.hardsdkdemo.ProductNeed.entity;

import java.io.Serializable;

/**
 * 作者：MrJiang on 2017/5/3 17:37
 */
public class Version implements Serializable {
    public String bluetoothVersion;
    public String braceletVersion;
    public String firmwareVersion;

    public String getBluetoothVersion() {
        return bluetoothVersion;
    }

    public void setBluetoothVersion(String bluetoothVersion) {
        this.bluetoothVersion = bluetoothVersion;
    }

    public String getBraceletVersion() {
        return braceletVersion;
    }

    public void setBraceletVersion(String braceletVersion) {
        this.braceletVersion = braceletVersion;
    }

    public String getFirmwareVersion() {
        return firmwareVersion;
    }

    public void setFirmwareVersion(String firmwareVersion) {
        this.firmwareVersion = firmwareVersion;
    }
}
